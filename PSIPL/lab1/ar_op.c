#include<stdio.h>

int main(){
    float a,b;
    printf("Enter 2 numbers: \n");
    scanf("%f%f", &a, &b);
    printf("Addition: %f\n", a+b);
    printf("Subtraction: %f\n", a-b);
    printf("Multiplication: %f\n", a*b);
    printf("Division: %f\n", a/b);
    printf("Modulus: %d\n", (int)a%(int)b);
}

